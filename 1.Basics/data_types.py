"""
Data Types in Python
1. Integer (int) -> 5,6,0, -99,66

2.Float (float) -> 55.56, 32.2224, 0.0, -0.05

3.Complex (complex) -> 5+6j

4.String (str) -> "Hello", 'World', "5", "5.5"

5.Boolean (bool) -> True, False

6.List (list) -> [1,2,3], ['a','b'], [True, False, True], [1, "ant", "man", 5.5], [[1,2,3], [4,5,6]]

7.Tuple (tuple) -> (1,2,3), ('a','b '), (True, False, True), (1, "ant", "man", 5.5), ((1,2,3), (4,5,6))

8.Set (set) -> {1,2,3}, {'a','b'}, {True, False, True}, {1, "ant", "man", 5.5}, {{1,2,3}, {4,5,6}}

9.Dictionary (dict) -> {"name":"John","age":30}

10.Bytes (bytes) -> b"Hello"

"""

a = 5
print(a, type(a))

b = 5.5
print(b, type(b))

c = 5 + 6j
print(c, type(c))

d = "Hello"
print(d, type(d))

e = True
print(e, type(e))
