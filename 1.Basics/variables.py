# Variables

"""
Variables are containers for storing data values
Python has no command for declaring a variable
A variable is created the moment you first assign a value to it. 
Syntax:  var_name = value
"""
a = 5
b = 10
print(a)
print(b)
print(a + b)

"""
Rules to name a variable:
1. A variable name must start with a letter a-z/ [A-Z] or an underscore (_)
2. A variable name can contain a-z, A-Z, _, 0-9
3. A variable name cannot start with a number
4. A variable name can not use reserved keywords(if-else)
5. Variable names are case-sensitive (age, Age, AGE are three different variables)
"""
__ = 15
___ = 10
print(__ + ___)
